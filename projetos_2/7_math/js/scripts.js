// função muito ultilizada


// pega o maior valor = max
let maior = Math.max(6, 10, 50, 43, 3, 5);

console.log(maior);

// pega o menor valor = min 
let menor = Math.min(3, 5, 60, 2, 2, 1);

console.log(menor);

// arredonda = round
let arrendondar = Math.round(5.29545);

console.log(arrendondar);

// arredondar pra cima = ceil
let arrendondarParaCima = Math.ceil(5.88375873);

console.log(arrendondarParaCima);